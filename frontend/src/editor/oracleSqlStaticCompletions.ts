import * as monaco from "monaco-editor";

const Kind = monaco.languages.CompletionItemKind;

/**
 * Monaco marks `range` required on CompletionItem, but the provider should set it from the
 * editor position when merging static items with dynamic results.
 */
export type OracleStaticCompletionItem = Omit<
  monaco.languages.CompletionItem,
  "range"
> & { range?: monaco.languages.CompletionItem["range"] };

/** Multi-word and special insert text for common SQL phrases. */
function kw(
  label: string,
  extra?: { insertText?: string; detail?: string; documentation?: string }
): OracleStaticCompletionItem {
  return {
    label,
    kind: Kind.Keyword,
    insertText: extra?.insertText ?? `${label} `,
    detail: extra?.detail,
    documentation: extra?.documentation,
    sortText: `0_${label}`,
  };
}

function fn(
  label: string,
  extra?: { detail?: string; documentation?: string }
): OracleStaticCompletionItem {
  return {
    label,
    kind: Kind.Function,
    insertText: `${label} `,
    detail: extra?.detail,
    documentation: extra?.documentation,
    sortText: `1_${label}`,
  };
}

function typ(label: string): OracleStaticCompletionItem {
  return {
    label,
    kind: Kind.Class,
    insertText: `${label} `,
    sortText: `2_${label}`,
  };
}

