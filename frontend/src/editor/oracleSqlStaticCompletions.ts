function dedupeKeywords(labels: readonly string[]): string[] {
  const seen = new Set<string>();
  const out: string[] = [];
  for (const l of labels) {
    const u = l.toUpperCase();
    if (!seen.has(u)) {
      seen.add(u);
      out.push(l);
    }
  }
  return out;
}

let cache: OracleStaticCompletionItem[] | null = null;

/**
 * All static Oracle SQL / PL-SQL completion items for merging with dynamic schema suggestions.
 * Result is cached for the lifetime of the app.
 */
export function getOracleStaticCompletions(): OracleStaticCompletionItem[] {
  if (cache) {
    return cache;
  }

  const kwTokens = dedupeKeywords([...SQL_KEYWORD_TOKENS, ...PLSQL_KEYWORD_TOKENS]);
  const fromTokens = kwTokens.map((label) =>
    kw(label, { insertText: `${label} ` })
  );

  const labelSet = new Set(
    fromTokens.map((i) => i.label.toString().toUpperCase())
  );
  const phrases = SQL_KEYWORD_PHRASES.filter(
    (p) => !labelSet.has(p.label.toString().toUpperCase())
  );

  const fnLabels = new Set(
    ORACLE_FUNCTIONS.map((f) => f.label.toString().toUpperCase())
  );
  const typeItems = ORACLE_TYPES.map((t) => typ(t)).filter((t) => {
    const u = t.label.toString().toUpperCase();
    return !fnLabels.has(u) && !labelSet.has(u);
  });

  cache = [...fromTokens, ...phrases, ...ORACLE_FUNCTIONS, ...typeItems];
  return cache;
}
